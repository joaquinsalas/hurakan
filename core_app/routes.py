@routes_bp.route('/')
@login_required
def show_latest_map_or_status():
    if MAINTENANCE_MODE:
        return render_template('maintenance.html')
    
    latest_processed_map = find_most_recent_processed_map()

    if latest_processed_map and os.path.exists(latest_processed_map):
        logger.info(f"Serving latest map: {os.path.basename(latest_processed_map)}")
        return send_file(latest_processed_map)
    else:
        logger.warning("No processed HTML maps found in output directory.")
        error_html = """
            <html><head><title>No Maps Available</title>
            <link rel="stylesheet" href="https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.0.0/css/all.min.css">
            <style> 
                body { font-family: sans-serif; display: flex; flex-direction: column; 
                       align-items: center; justify-content: center; height: 90vh; color: #6c757d; } 
                .icon { font-size: 3em; margin-bottom: 20px; } 
            </style>
            </head><body>
            <i class="fas fa-map-marker-alt icon"></i>
            <h2>No Maps Available</h2>
            <p>The system is waiting for new data or initial processing. Please try again in a few minutes.</p>
            </body></html>
            """
        return render_template_string(error_html), 503

@routes_bp.route('/upload_map', methods=['POST'])
def upload_map():
    """Endpoint for the monitor service to upload combined HTML maps."""
    if 'map_file' not in request.files:
        logger.error("Missing 'map_file' in upload request.")
        return jsonify(message="No map file provided"), 400

    map_file = request.files['map_file']
    if map_file.filename == '':
        logger.error("Empty filename in upload request.")
        return jsonify(message="Empty map filename"), 400

    try:
        map_output_dir = os.getenv('MAP_OUTPUT_DIR', '/tmp')
        save_path = os.path.join(map_output_dir, map_file.filename)
        map_file.save(save_path)
        logger.info(f"Map '{map_file.filename}' saved successfully to {save_path}")
        return jsonify(message=f"Map '{map_file.filename}' received successfully"), 200
    except Exception as e:
        logger.error(f"Error saving uploaded map '{map_file.filename}': {e}", exc_info=True)
        return jsonify(message=f"Error saving map: {str(e)}"), 500

