# Helper Logic
# ==============================================================================

def find_most_recent_processed_map():
    """Locates the latest HTML map file generated by the monitor service."""
    # Maps are expected to follow the pattern 'map_YYYYMMDD_HH.html'
    map_output_dir = os.getenv('MAP_OUTPUT_DIR', '/tmp')
    map_pattern = os.path.join(map_output_dir, 'map_*.html')
    list_of_maps = glob.glob(map_pattern)
    
    if not list_of_maps:
        return None

    try:
        # Sort by modification time to retrieve the latest one
        latest_map = max(list_of_maps, key=os.path.getmtime)
        return latest_map
    except Exception as e:
        logger.error(f"Error searching for the latest processed map: {e}")
        return None

