@routes_bp.route('/login', methods=['GET', 'POST'])
def login():
    if current_user.is_authenticated:
        return redirect(url_for('routes.show_latest_map_or_status'))

    form = LoginForm()
    if form.validate_on_submit():
        user = User.query.filter_by(username=form.username.data).first()
        
        if user is None or not user.check_password(form.password.data):
            flash('Invalid username or password.', 'danger')
            logger.warning(f"Failed login attempt for user: {form.username.data}")
            return redirect(url_for('routes.login'))

        login_user(user, remember=form.remember_me.data)
        logger.info(f"User {user.username} logged in successfully.")

        next_page = request.args.get('next')
        if not next_page or not next_page.startswith('/'):
            next_page = url_for('routes.show_latest_map_or_status')
        return redirect(next_page)
        
    return render_template('login.html', title='Hurakan - Login', form=form)

@routes_bp.route('/logout')
@login_required
def logout():
    logger.info(f"User {current_user.username} logged out.")
    logout_user()
    flash('You have been logged out.', 'info')
    return redirect(url_for('routes.login'))

